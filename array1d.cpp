#include <iostream>
using namespace std;

int main() {
    // Array declaration and initialization
    int arr[5] = {4, 12, 7, 15, 9};
    // Iterate over the array
    for (int i = 0; i < 5; i++) {
        // Print out each element in a new line
        cout << arr[i] << endl;
    }
    return 0;
}
